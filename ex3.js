
// Solicitar ao usuario os parametros de largura e altura do retangulo
let largura = Number(prompt("Digite a Largura do retangulo"));
let altura = Number(prompt("Digite a Altura do retangulo"));

// Calcular multiplicando a largura pela altura para obter o resultado da área do retangulo
area = (largura * altura);

// Informar ao usuario o resultado da area do retangulo
console.log ("A area do retangulo é " + area + "cm");