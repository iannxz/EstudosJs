//  𝐹= ((𝐶×9) /5) + 32 

// Solicitar ao usuario a temperatura em Celsius para ser convertida para Fahrenheit
let tempCelsius = Number(prompt("Digite a temperatura em Celsius para ser convertido para Fahrenheit"));

// Converter a temperatura em Celsius para Fahrenheit
conversao = (tempCelsius * 9) /5 + 32 

// Informar ao usuario o resultado da conversão de Celsius para Fahrenheit
console.log ("A temperatura de " + tempCelsius + " Graus convertendo para Fahrenheit é " + conversao);

